def read_text(path: Path) -> str:
    return path.read_text(encoding="utf-8")


def run_builder(*args: str) -> subprocess.CompletedProcess[str]:
    return subprocess.run(
        [sys.executable, "-m", "scripts.build_train_gpt_mlx", *args],
        cwd=REPO_ROOT,
        check=False,
        capture_output=True,
        text=True,
    )


def top_level_def_names(path: Path) -> set[str]:
    module = ast.parse(read_text(path))
    return {
        node.name
        for node in module.body
        if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef, ast.ClassDef))
    }


def assert_type_checking_scaffold(module: ast.Module) -> ast.If:
    assert len(module.body) >= 3

    future_import = module.body[0]
    typing_import = module.body[1]
    scaffold_gate = module.body[2]

    assert isinstance(future_import, ast.ImportFrom)
    assert future_import.module == "__future__"
    assert future_import.names[0].name == "annotations"

    assert isinstance(typing_import, ast.ImportFrom)
    assert typing_import.module == "typing"
    assert typing_import.names[0].name == "TYPE_CHECKING"

    assert isinstance(scaffold_gate, ast.If)
    assert isinstance(scaffold_gate.test, ast.Name)
    assert scaffold_gate.test.id == "TYPE_CHECKING"

    top_level_imports = [
        node for node in module.body if isinstance(node, (ast.Import, ast.ImportFrom))
    ]
    assert len(top_level_imports) == 2
    return scaffold_gate


