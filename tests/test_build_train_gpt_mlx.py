    assert text.index("def loss_and_grad_chunked") < text.index("def token_chunks")
    assert text.index("def token_chunks") < text.index("def setup_tokenizer_and_validation")
    assert text.index("class SplitOptimizers") < text.index("def setup_tokenizer_and_validation")
    assert text.index("def finalize_and_validate_roundtrip") < text.index("def main()")
    assert text.index("def clip_grad_tree") < text.index("def main()")
