def test_generated_train_gpt_mlx_has_no_live_sibling_imports() -> None:
    module = ast.parse(read_text(GENERATED_ROOT))
    sibling_imports = {
        node.module
        for node in module.body
        if isinstance(node, ast.ImportFrom)
        and node.module is not None
        and node.module.startswith("train_gpt_mlx_src")
    }
    sibling_imports.update(
        alias.name
        for node in module.body
        if isinstance(node, ast.Import)
        for alias in node.names
        if alias.name.startswith("train_gpt_mlx_src")
    )
    assert not sibling_imports, sibling_imports


def test_generated_train_gpt_mlx_rebuild_is_byte_idempotent() -> None:
    first_run = run_builder()
    assert first_run.returncode == 0, first_run.stdout + first_run.stderr
    first_bytes = GENERATED_ROOT.read_bytes()

    second_run = run_builder()
    assert second_run.returncode == 0, second_run.stdout + second_run.stderr
    second_bytes = GENERATED_ROOT.read_bytes()

    assert first_bytes == second_bytes, "builder is not byte-idempotent across consecutive rebuilds"


